function y_pos(day) {
  var pos;
  switch (day){
    case 'Mon':
      pos = 1;
      break;
    case 'Tue':
      pos = 2;
      break;
    case 'Wed':
      pos = 3;
      break;
    case 'Thur':
      pos = 4;
      break;
    case 'Fri':
      pos = 5;
      break;
    case 'Sat':
      pos = 6;
      break;
    case 'Sun':
      pos = 7;
      break;
    default:
      console.log("Invalid input. I love Alex");
  }
  return pos;
}

var margin = 50;
var width = 1000;
var height = 450;

function drawHourlyData(hourlyData) {
  d3.select("body")
    .append("svg")
      .attr("width", width)
      .attr("height", height)
    .selectAll("circle")
    .data(hourlyData)
    .enter()
    .append("circle");

  var x_extent = [-1, 24];

  var x_scale = d3.scale.linear()
    .range([margin,width-margin])
    .domain(x_extent);

  var y_extent = [0,8];

  var y_scale = d3.scale.linear()
    .range([height-margin, margin])
    .domain(y_extent);

  d3.selectAll("circle")
    .attr("cx", function(d){return x_scale(d.time);})
    .attr("cy", function(d){return y_scale(y_pos(d.day));})
    .attr("r", function(d){return d.noise * 10;});

  var x_axis = d3.svg.axis().scale(x_scale);

  d3.select("svg")
    .append("g")
      .attr("class", "x axis")
      .attr("transform", "translate(0," + (height-margin) + ")")
    .call(x_axis);

  var y_axis = d3.svg.axis().scale(y_scale).orient("left");

  d3.select("svg")
    .append("g")
      .attr("class", "y axis")
      .attr("transform", "translate(" + margin + ", 0 )")
    .call(y_axis);

  d3.select(".x.axis")
    .append("text")
      .attr("class", "axis_label")
    .text("datetime")
      .attr("x", (width / 2) - margin)
      .attr("y", margin / 1.5);

  d3.select(".y.axis")
    .append("text")
      .attr("class", "axis_label")
    .text("noise(dB)")
      .attr("transform", "rotate (-90, -43, 0) translate(-280)");

}
